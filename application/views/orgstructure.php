                                <br><br><h2 class="text-left" style="font-size:16px; opacity: 0.7;"><i>OADR DBM Approved Organizational Structure</i></h2><br>
                                <p style = "text-align:justify;"><b>Office of the Executive Director</b></p>
                                <ul style="list-style-type: square; padding-left:20px;">
                                &nbsp;<li><p style = "text-align:justify;">Approves plans, policies and programs that will guide the operations of the QADR consistent with policies and programs of the DOJ.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Recommends to the Secretary of Justice the members of the Advisory Council; Convenes the Advisory Council regularly or as needed.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Submits the proposed annual budget of the OADR through the Secretary of Justice; responsible and accountable for the administration of the authorized budget of the OADR.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Appoints individuals to authorized positions within the staffing pattern of the OADR and enforce national government laws on its personnel.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Initiates programs and other acceptable practices that will promote productivity, efficiency, and effectiveness in the Office.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Provides leadership and overall direction of the operations of the OADR and exercise day to day supervision of operations of the service and divisions within the OADR.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Provides secretariat services to the Advisory Council; and Drafts opinions.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Replies to private queries and decisions and establishes a grievance machinery procedure by formulating the appropriate ADR mechanism; and handles administrative investigations and decides on administrative cases as performed by the legal staff within the Office.</p></li>
                                </ul>

                                <p style = "text-align:justify;"><b>Finance and Administrative Division</b></p>
                                <ul style="list-style-type: square; padding-left:20px;">
                                &nbsp;<li><p style = "text-align:justify;">Assists the OED in the formulation and implementation of policies, standards, rules and procedures that will promote the sound planning and management of the OADR's resources, i.e., its financial, human, physical and technological facets.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Safeguards the revenues of the OADR and its services; Formulates, recommends and implements approved annual OADR budget including the preparation and execution of the approved allotment and cash programs, disbursements, accounting and reporting thereof.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Formulates, recommends and implements the approved physical assets plan and procurement program with proper distribution, maintenance, accounting and disposal thereof.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Formulates, recommends and implements the approved personnel management policies, programs, rules and procedures including the personnel administration of the OADR's personnel.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">repares financial reports and other administrative reports which the Executive Director and oversight agencies of the national government may require.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">and Provision of general services to the OADR officers and personnel.</p></li>
                                </ul>

                                <p style = "text-align:justify;"><b>Training, Accreditation and Promotion Service</b></p>
                                <p style = "text-align:justify;"><b>Training and Program Development Division</b></p>
                                <ul style="list-style-type: square; padding-left:20px;">
                                &nbsp;<li><p style = "text-align:justify;">Formulates standards for training of Alternative Dispute Resolution practitioners and providers.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Establishes training programs for ADR providers and practitioners both in the public and private sectors.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Undertakes periodic and continuous training programs for arbitration and mediation. </p></li>
                                &nbsp;<li><p style = "text-align:justify;">Establishes partnerships and cooperative mechanisms for the delivery of training programs with established ADR, civil society and/ or training institutions both local and international.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Conducts continuous research and development on training to promote improvement of training standards programs.</p></li>
                                </ul>

                                <p style = "text-align:justify;"><b>Training and Program Development Division</b></p>
                                <ul style="list-style-type: square; padding-left:20px;">
                                &nbsp;<li><p style = "text-align:justify;">Formulates and implements an accreditation and certification scheme for qualified individual and institutional ADR providers;</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Issues accreditation certifications to accredited ADR institutions and individuals; Establishes and regularly publishes a list of accredited and certified ADR institutions and individuals.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Establishes and maintains a list or roster of foreign or international ADR providers and practitioners.</p></li>
                                </ul>

                                <p style = "text-align:justify;"><b>Public Information and Promotion Division</b></p>
                                <ul style="list-style-type: square; padding-left:20px;">
                                &nbsp;<li><p style = "text-align:justify;">Promotes, develops and expands the use of ADR in the private and public sectors through information, education and communication.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Formulates and implements an information, education and communication strategy to promote the use of ADR and to improve citizen access to quality ADR services; Conducts seminars, symposia, conferences and other public fora.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Publish proceedings and other relevant materials thereof that will promote, develop and expand the use of ADR; Develops partnerships with civil society, academe and ADR institutions to improve information dissemination on ADR and its services.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Establishes an ADR library or resource center where ADR laws, rules and regulations, jurisprudence, books, articles and other information about ADR in the Philippines and elsewhere may be stored and accessed.</p></li>
                                </ul>

                                <p style = "text-align:justify;"><b>Policy, Compliance and Monitoring Service</b></p>
                                <p style = "text-align:justify;"><b>Policy Formulation and Development Division</b></p>
                                <ul style="list-style-type: square; padding-left:20px;">
                                &nbsp;<li><p style = "text-align:justify;">Monitors, studies and evaluates ADR use in the private and public sectors and recommends policies, legislative measures, programs and projects to improve ADR and its use.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Undertakes research on ADR towards continual improvement of rules, procedures, institutional frameworks, technologies and service delivery management.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Continuous review and formulation of recommendations on new or improved policies, rules, organization and delivery systems on OADR functions.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Acts as the technical secretariat in the OADR strategic and operational planning formulation and monitoring such as performance evaluation and recommendation; Undertakes studies and develops standards, guidelines and procedures that will guide the operations of the OADR's core functions including training partnership development approaches, access and service delivery, pricing and fee collection, and related areas.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Develops an indicator and evaluation system including operations and performance evaluation approaches and methodologies of the country's ADR system in general and the performance of the OADR in particular and Prepares the OADR's annual monitoring and performance report.</p></li>
                                </ul>

                                <p style = "text-align:justify;"><b>Compliance and Monitoring Division</b></p>
                                <ul style="list-style-type: square; padding-left:20px;">
                                &nbsp;<li><p style = "text-align:justify;">Formulates and implements an effective monitoring scheme to maintain the quality of ADR providers and practitioners in the accredited and certified roster.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Updates the Accreditation and Certification Division regarding the continuing quality and standards of those ADR providers and practitioners in the roster of accredited and certified ADR practitioners and providers.</p></li>
                                &nbsp;<li><p style = "text-align:justify;">Alerts the Accreditation and Certification Division of those ADR providers and practitioners who fell short of the standards with the appropriate recommendation to strike out or delist the said ADR provider or practitioner, and Collaborates with the Accreditation and Certification Division to harmonize their standard operating procedures and rules regarding the roster.</p></li>
                                </ul>
